# Working with numbers
# Commonly used math functions

from math import *

# Output number
print(2)

# Output decimal
print(2.3425)
print(-2.345345)

# Addition
print(3 + 5)

# Substraction
print(4 - 3)

# Multiplication
print(3 * 4)

# PEMDAS
print(3 * (4 + 5))

# Modulus aka remainder 
print(10 % 3)

# Can store numbers in variables
my_num = 5 
print(my_num)

# Converting numbers into strings
print(str(my_num))

# Useful for printing numbers alongside strings
print(str(my_num) + " my favorite number")

# Perform functions on numbers

#ABS: absolute value
print(abs(my_num))

#POW: passes two peices of information, first being a number, second being the power you wish to take it to 
print(pow(4, 6))

#MAX: returns larger of the two numbers passed
print(max(4, 6))

#MIN: returns smaller of the two numbers passed
print(min(4, 6))

#ROUND: rounds number to the nearest whole number
print(round(3.7))

#FLOOR: Grabs the lowest number 
print(floor(3.7))

#CEIL: Grabs the highest number
print(ceil(3.7))

#SQRT: Returns the square root of the number 
print(sqrt(36))


